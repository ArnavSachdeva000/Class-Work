// Function multiply() for:
// Two integers
// Three integers
// Two doubles

#include<iostream>
using namespace std;

void multiply(int a,int b)
{
    cout<<"Product of two integers: "<<a*b<<endl;
}

void multiply(int a,int b,int c)
{
    cout<<"Product of three integers: "<<a*b*c<<endl;
}

void multiply(double a,double b)
{
    cout<<"Product of two doubles: "<<a*b<<endl;
}

